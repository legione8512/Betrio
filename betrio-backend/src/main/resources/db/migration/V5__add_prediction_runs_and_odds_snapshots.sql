CREATE TABLE prediction_run (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fixture_id BIGINT NOT NULL REFERENCES fixture(id) ON DELETE CASCADE,
    model_version VARCHAR(50) NOT NULL,
    generated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    expected_home_goals DOUBLE PRECISION,
    expected_away_goals DOUBLE PRECISION,
    home_win_probability DOUBLE PRECISION NOT NULL,
    draw_probability DOUBLE PRECISION NOT NULL,
    away_win_probability DOUBLE PRECISION NOT NULL,
    over25_probability DOUBLE PRECISION NOT NULL,
    under25_probability DOUBLE PRECISION NOT NULL,
    btts_yes_probability DOUBLE PRECISION NOT NULL,
    btts_no_probability DOUBLE PRECISION NOT NULL
);

CREATE INDEX idx_prediction_run_fixture ON prediction_run(fixture_id);
CREATE INDEX idx_prediction_run_generated_at ON prediction_run(generated_at);

CREATE TABLE prediction_exact_score (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prediction_run_id BIGINT NOT NULL REFERENCES prediction_run(id) ON DELETE CASCADE,
    home_goals INTEGER NOT NULL,
    away_goals INTEGER NOT NULL,
    probability DOUBLE PRECISION NOT NULL
);

CREATE INDEX idx_prediction_exact_score_run ON prediction_exact_score(prediction_run_id);

CREATE TABLE odds_snapshot (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fixture_id BIGINT NOT NULL REFERENCES fixture(id) ON DELETE CASCADE,
    bookmaker_id BIGINT,
    bookmaker_name VARCHAR(150),
    market_name VARCHAR(150) NOT NULL,
    outcome_name VARCHAR(150) NOT NULL,
    odd_value DOUBLE PRECISION,
    captured_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_odds_snapshot_fixture ON odds_snapshot(fixture_id);
CREATE INDEX idx_odds_snapshot_market ON odds_snapshot(fixture_id, market_name);