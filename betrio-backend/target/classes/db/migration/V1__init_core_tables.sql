CREATE TABLE competition (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_name VARCHAR(50) NOT NULL,
    external_league_id BIGINT NOT NULL,
    country_name VARCHAR(100),
    competition_name VARCHAR(150) NOT NULL,
    competition_type VARCHAR(50),
    logo_url TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_competition_provider_external
        UNIQUE (provider_name, external_league_id)
);

CREATE TABLE season (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    competition_id BIGINT NOT NULL REFERENCES competition(id) ON DELETE CASCADE,
    external_season_year INTEGER NOT NULL,
    start_date DATE,
    end_date DATE,
    current_flag BOOLEAN NOT NULL DEFAULT FALSE,
    coverage_json JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_season_competition_year
        UNIQUE (competition_id, external_season_year)
);

CREATE TABLE sync_job (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_name VARCHAR(50) NOT NULL,
    job_type VARCHAR(100) NOT NULL,
    target_type VARCHAR(50),
    target_external_id BIGINT,
    status VARCHAR(30) NOT NULL,
    started_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    finished_at TIMESTAMPTZ,
    message TEXT
);

CREATE TABLE raw_api_response (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_name VARCHAR(50) NOT NULL,
    endpoint_path VARCHAR(255) NOT NULL,
    request_query VARCHAR(1000),
    target_type VARCHAR(50),
    target_external_id BIGINT,
    payload JSONB NOT NULL,
    fetched_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_raw_api_response_target
    ON raw_api_response (provider_name, target_type, target_external_id);

CREATE INDEX idx_sync_job_status
    ON sync_job (provider_name, job_type, status);