CREATE TABLE prediction_evaluation (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prediction_run_id BIGINT NOT NULL UNIQUE REFERENCES prediction_run(id) ON DELETE CASCADE,
    evaluated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    actual_home_goals INTEGER NOT NULL,
    actual_away_goals INTEGER NOT NULL,

    predicted_result_code VARCHAR(10) NOT NULL,
    actual_result_code VARCHAR(10) NOT NULL,

    actual_over25 BOOLEAN NOT NULL,
    actual_btts_yes BOOLEAN NOT NULL,

    hit_1x2 BOOLEAN NOT NULL,
    hit_over25 BOOLEAN NOT NULL,
    hit_btts BOOLEAN NOT NULL,
    top_exact_score_hit BOOLEAN NOT NULL,

    brier_score_1x2 DOUBLE PRECISION NOT NULL,
    log_loss_1x2 DOUBLE PRECISION NOT NULL,

    market_home_implied_probability DOUBLE PRECISION,
    market_draw_implied_probability DOUBLE PRECISION,
    market_away_implied_probability DOUBLE PRECISION,

    model_edge_home DOUBLE PRECISION,
    model_edge_draw DOUBLE PRECISION,
    model_edge_away DOUBLE PRECISION
);

CREATE INDEX idx_prediction_evaluation_run ON prediction_evaluation(prediction_run_id);
CREATE INDEX idx_prediction_evaluation_evaluated_at ON prediction_evaluation(evaluated_at);