CREATE TABLE manual_action_run (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    action_key VARCHAR(100) NOT NULL,
    status VARCHAR(30) NOT NULL,
    fixture_id BIGINT REFERENCES fixture(id) ON DELETE SET NULL,
    started_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    finished_at TIMESTAMPTZ,
    message TEXT
);

CREATE INDEX idx_manual_action_run_started_at ON manual_action_run(started_at);
CREATE INDEX idx_manual_action_run_fixture_id ON manual_action_run(fixture_id);
CREATE INDEX idx_manual_action_run_action_key ON manual_action_run(action_key);